import 'dart:io';

import 'package:args/args.dart';
import 'package:file/local.dart';
import 'package:file/memory.dart';
import 'package:openapigen/openapigen.dart';
import 'package:openapigen/src/logger.dart';
import 'package:path/path.dart' as p;

void main(List<String> args) async {
  // parse args
  final argResult = _argParser.parse(args);

  if (argResult.wasParsed('help')) {
    print(_argParser.usage);
    exitCode = 0;
    return;
  }

  final logger = argResult.wasParsed('verbose') ? baseLogger : verboseLogger;
  logger.onRecord.listen(stdoutListener);

  try {
    // get openapi spec file
    if (argResult.rest.isEmpty) {
      throw Exception('Expected at least one argument: the openapi spec file');
    }

    final openapiSpec = argResult.rest.first;

    final fs = argResult.wasParsed('memory')
        ? MemoryFileSystem()
        : const LocalFileSystem();

    // read openapi spec
    logger.info('Begin parsing OpenAPI spec from file: $openapiSpec');
    final openAPI = await parseOpenAPI(openapiSpec, fs: fs, logger: logger);
    logger.info('Finished parsing OpenAPI spec');

    final outputFile =
        argResult['output'] as String? ?? p.join(p.current, 'openapi.dart');

    // perform generation on openapi spec
    logger.info('Begin generating code from OpenAPI spec');
    final code = generateSchemaFromOpenAPI(openAPI, outputFile: outputFile);
    logger.info('Finished generating code from OpenAPI spec');

    // write to file
    logger.info('Writing generated code to file: $outputFile');
    await fs.file(outputFile).writeAsString(code);

    // optionally run build_runner
    if (argResult['build-generate'] as bool) {
      logger.info(
        'Running build_runner in output directory: ${p.dirname(outputFile)}',
      );
      final outputDir = p.dirname(outputFile);
      final result = await Process.run('dart', [
        'run',
        'build_runner',
        'build',
        '--delete-conflicting-outputs',
      ], workingDirectory: outputDir);
      if (result.exitCode != 0) {
        logger.severe('Error running build_runner: ${result.stderr}');
        exitCode = result.exitCode;
      } else {
        logger.info('build_runner completed successfully');
      }
    } else {
      logger.info('Generation complete. Output written to $outputFile');
    }
  } catch (e, stack) {
    logger.severe('Error', e, stack);
  } finally {
    logger.clearListeners();
  }
}

final _argParser = ArgParser(allowTrailingOptions: true)
  ..addOption(
    'output',
    abbr: 'o',
    help: 'The output file to write the interface to',
  )
  ..addOption(
    'schema-name',
    abbr: 's',
    help: 'The name for the generated interface (optional)',
  )
  ..addFlag('memory', negatable: false, hide: true)
  ..addFlag('help', abbr: 'h', negatable: false, help: 'Show help information')
  ..addFlag(
    'verbose',
    abbr: 'v',
    negatable: false,
    help: 'Enable verbose logging',
  )
  ..addFlag(
    'build-generate',
    help:
        'Run build_runner in the output directory of the files generated by this tool',
    negatable: false,
    defaultsTo: false,
  );
